package com.usa.smarttech;

public class MyFirstJavaClass {
	public static void main(String[] args) {
		System.out.println("");
		
		boolean teresa = true;
		System.out.println(teresa);
		boolean ter =false;
		System.out.println(ter);
		
		//char handle anything within "only one word;
		char com ='c';
		//holds characters.size:2bytes
		System.out.println(com);
		
		int x =2000;
		//its range:-2566 to 566
		System.out.println(x);
		
		
		
		
		
		
		//Double can handle decimal number
		double a = 200.33;
		System.out.println(a);
		
		//float can handle 6 to 7 decimal number
		float num5 = 200.993F;  
		System.out.println(num5);
		
		//long can handle large number and default size 8 bytes
		long A = 1088888888888522112L;
		System.out.println(A);
		
		//string handle anything within ""
		String Do = "Vote for Trump";
		
		System.out.println("Do");
		
		
		
		char US2323222222222A = 'z';
		System.out.println(US2323222222222A);
		
		
		
		
				
	}}

		
		